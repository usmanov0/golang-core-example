package main

import "fmt"

func operators2() {
	a := 14
	b := 30

	// ‘=='(equal)
	result1 := a == b
	fmt.Println(result1)

	// ‘!='(not equal)
	result2 := a != b
	fmt.Println(result2)

	// ‘<‘(less than)
	result3 := a < b
	fmt.Println(result3)

	// ‘>'(greter than)
	result4 := a > b
	fmt.Println(result4)

	// ‘>='(greater than or equal to)
	result5 := a >= b
	fmt.Println(result5)

	// ‘<='(less than or equal to)
	result6 := a <= b
	fmt.Println(result6)

}
